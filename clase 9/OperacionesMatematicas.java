public class OperacionesMatematicas {
//atributos
    private int n1;
    private int n2;

    //constructor de la clase

    public OperacionesMatematicas(int n1, int n2){
        this.n1 = n1;
        this.n2 = n2;
    }
    
    //metodos
    public int sumar(){
        return n1 + n2;
    }

    
}
